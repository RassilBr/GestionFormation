    public void remplir(int x)
    {
          
           String sql= "select etat from demande where id_emp="+x;
           
           try{
           ResultSet rs=Rselect.select(sql);
           
           if(rs.next()){
                    String etat=rs.getString("etat");
                    jEtat.setText(etat);
                    btnEffectuer.setEnabled(false);
                    date1.setEnabled(false);
                    date2.setEnabled(false);
                    switch(etat)
                       {
                        case "Traitement .." : jEtat.setForeground(Color.BLUE) ;
                            break;
                        case "Refusée" : jEtat.setForeground(Color.RED) ;
                            break;
                        case "Acceptée" : jEtat.setForeground(Color.GREEN) ;
                                          jChoix.setEnabled(true);
                                          
                            break;
                       }
           }
           
           
          }catch(SQLException ex){
           JOptionPane.showMessageDialog(this, ex);}
           
           

      
           
       
    }
    
