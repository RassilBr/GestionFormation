    public void AjoutDemande(int x){
    
       
        String jdate1=convdate(date1.getText());
        String jdate2=convdate(date2.getText());
        String sql= "select etat from demande where id_emp="+x;
        ResultSet rs=Rselect.select(sql);
        try {
            if(rs.next())
            {      
                    JOptionPane.showMessageDialog(this,"Vous avez deja une demande");
                    return;
            }
            else{
                
                 sql= "INSERT INTO `demande` (`id_emp`"
                       + ", `date_a`, `date_b`, `etat`) VALUES ('"+x+"', '"
                       +jdate1+"', '"+jdate2+"', 'Traitement ..')";
           Rselect.Insert(sql);
           JOptionPane.showMessageDialog(this,"Votre demande est bien envoyé");
           remplir(x);
            }
            
            
            
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex);
        }
        
               

     }
    
    public String convdate(String ch)
    {
    return "20"+ch.charAt(6)+ch.charAt(7)+"-"+ch.substring(3,5)+"-"+ch.substring(0,2);
    }
    
    
