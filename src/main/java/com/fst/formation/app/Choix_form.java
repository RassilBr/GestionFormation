    public void remplir(){

        String sql="select formation.id_form from formation INNER JOIN session INNER JOIN inscrit ON session.id_form=formation.id_form AND session.id_sess=inscrit.id_sess where inscrit.id_emp="+id_emp;
        Vector<Integer> vsess=new Vector<>();
        ResultSet rs=Rselect.select(sql);  
        
        try{
          while(rs.next()){
           vsess.add( rs.getInt(1) );
           
            }
        
        }catch(SQLException ex){
        JOptionPane.showMessageDialog(this, ex);
        } 
        
     
          String sql1="select * from formation";
        
        
            
         if(!vsess.isEmpty())
         {
                 sql1+=" where id_form NOT Like '"+vsess.elementAt(0)+"'";
            for(int i=1;i<vsess.size()-2;i++){
               
               sql1+=" AND id_form NOT Like '"+vsess.elementAt(i)+"' ";            
            }
            sql1+=" AND id_form NOT Like '"+vsess.lastElement()+"' "; 
            
         }   
        else System.out.println("Empty");
       
        
        System.out.println(sql1);
        
        
        

          
       ResultSet rs1=Rselect.select(sql1);
        form_tab.setModel(DbUtils.resultSetToTableModel(rs1));
        

    

    
    
    
    }
    

